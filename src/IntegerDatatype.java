public class IntegerDatatype {
    public static void main(String[] args) {
        byte number = 1;
        System.out.println(number);
        number = 3;
        System.out.println(number);

        int numberTwo = 20;
        System.out.println(numberTwo);
        numberTwo = number + 10;
        System.out.println(numberTwo);
    }
}
